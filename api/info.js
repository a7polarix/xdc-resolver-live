// ENDPOINT /api/info (UNIQUE, avec copyright)
// ============================================================
export default async function handler(req, res) {
    res.setHeader('Access-Control-Allow-Origin', '*');
    res.setHeader('Cache-Control', 'no-store');

    const { domain } = req.query;
    if (!domain) return res.status(400).json({ error: 'Domaine manquant' });

    const resolveUrl = `https://fleurs-resolver-final.vercel.app/api/resolve?domain=${encodeURIComponent(domain)}`;
    try {
        const resolveRes = await fetch(resolveUrl);
        const resolveData = await resolveRes.json();
        if (!resolveData.result) {
            return res.status(404).json({ error: 'Domaine non trouvé' });
        }
        const category = getCategory(domain);
        const response = {
            domain,
            address: resolveData.result,
            category: category || null,
            assetType: category ? 'SemanticAsset' : null,
            endpoints: {
                info: `/api/info?domain=${encodeURIComponent(domain)}`,
                invoice: `/api/invoice?domain=${encodeURIComponent(domain)}`,
                status: '/api/status'
            },
            copyright: "© 2026 Fleurs de Lys / FocalZero – Tous droits réservés – Licence SAN-2026.1"
        };
        res.status(200).json(response);
    } catch (err) {
        console.error(err);
        res.status(500).json({ error: 'Erreur serveur' });
    }
}