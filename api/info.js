// FONCTION DE CATÉGORISATION (avec héritage)
// ============================================================
function getCategory(domain) {
    if (!domain) return null;
    const d = domain.toLowerCase();
    if (DOMAIN_CATEGORY[d]) return DOMAIN_CATEGORY[d];
    let parts = d.split('.');
    while (parts.length > 1) {
        parts.shift();
        const parent = parts.join('.');
        if (DOMAIN_CATEGORY[parent]) return DOMAIN_CATEGORY[parent];
    }
    if (d.endsWith('.xdc')) return "Domaine XDC";
    if (d.endsWith('.rwa')) return "Real World Asset";
    if (d.endsWith('.depin')) return "DePIN";
    return null;
}

